    private void jbAlterarCadastroVagasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarCadastroVagasActionPerformed
        
        try{ 
            for ( int i=0; i < this.listaVaga.size(); i++ ){
                if ( jtVagaCadastroVaga.getSelectedRow() == i ){
                    vagaAlteracao = new Vaga();
                    vagaAlteracao.setNumeroVaga(this.listaVaga.get(i).getNumeroVaga());
                    vagaAlteracao.setId(this.listaVaga.get(i).getId());
                    vagaAlteracao.setTipoVaga(this.listaVaga.get(i).getTipoVaga());
                    vagaAlteracao.setTipovalor(this.listaVaga.get(i).getTipoValor());
                    vagaAlteracao.setValorUnit(this.listaVaga.get(i).getValorUnit());
                    jtfVagaCadastroVaga.setText(new Integer(this.listaVaga.get(i).getNumeroVaga()).toString());
                    jcbTipoCadastroVaga.setSelectedItem(this.listaVaga.get(i).getTipoVaga());
                    jcbTipoValor.setSelectedItem(this.listaVaga.get(i).getTipoValor());
                    jtfValorCadastroVaga.setText(new Double (this.listaVaga.get(i).getValorUnit()).toString());
                    
                   
                    
                }
            }
       } catch ( Exception e){
           JOptionPane.showMessageDialog(null, e.getMessage());
       }
       atualizarTabelaVagas();
                                                    
                                                         
        
    
    }//GEN-LAST:event_jbAlterarCadastroVagasActionPerformed

    private void jbDeletarCadastroVagaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbDeletarCadastroVagaActionPerformed
     
          try {
          
            this.listaVaga = VagaController.obterInstancia().listarTodos();
            VagaController.obterInstancia().
                    remover(this.listaVaga.get(jtVagaCadastroVaga.getSelectedRow()));
            
        } catch ( ArrayIndexOutOfBoundsException a){
            JOptionPane.showMessageDialog(null, "Selecione uma vaga");
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        } 
        atualizarTabelaVagas();
                                                
                                           
        
        
    }//GEN-LAST:event_jbDeletarCadastroVagaActionPerformed


