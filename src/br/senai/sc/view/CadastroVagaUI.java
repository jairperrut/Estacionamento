    private void jbSalvarCadastroVagaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarCadastroVagaActionPerformed
   
         try {
            if ( vagaAlteracao != null ) {
                vagaAlteracao.setNumeroVaga(Integer.parseInt(jtfVagaCadastroVaga.getText().trim()));
                vagaAlteracao.setTipoVaga(jcbTipoCadastroVaga.getSelectedItem().toString());
                vagaAlteracao.setTipovalor(jcbTipoValor.getSelectedItem().toString());
                vagaAlteracao.setValorUnit(new Double(jtfValorCadastroVaga.getText()).doubleValue());
                VagaController.obterInstancia().alterar(vagaAlteracao);
                JOptionPane.showMessageDialog(null, "Vaga alterada com sucesso");
            } else {
                Vaga vaga = new Vaga();
                vaga.setNumeroVaga(Integer.parseInt(jtfVagaCadastroVaga.getText().trim()));
                vaga.setTipoVaga(jcbTipoCadastroVaga.getSelectedItem().toString());
                vaga.setTipovalor(jcbTipoValor.getSelectedItem().toString());
                vaga.setValorUnit(new Double(jtfValorCadastroVaga.getText()).doubleValue());
                VagaController.obterInstancia().inserir(vaga);
                atualizarTabelaVagas();
                JOptionPane.showMessageDialog(null, "Vaga cadastrada com sucesso");
                
            }
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage() );

        }
    }//GEN-LAST:event_jbSalvarCadastroVagaActionPerformed

