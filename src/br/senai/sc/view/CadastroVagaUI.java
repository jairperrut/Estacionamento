     public void atualizarTabelaVagas(){
        DefaultTableModel modeloVaga = new DefaultTableModel();
        modeloVaga.setColumnIdentifiers(new String[] {"Vaga","Tipo Vaga","Tipo Valor","Valor R$"});
        this.listaVaga = VagaController.obterInstancia().listarTodos();
        for ( int i=0; i < this.listaVaga.size(); i++){
            modeloVaga.addRow(new Object[] { 
                this.listaVaga.get(i).getNumeroVaga(),
                this.listaVaga.get(i).getTipoVaga(),
                this.listaVaga.get(i).getTipoValor(),
                this.listaVaga.get(i).getValorUnit()});
        }
        jtVagaCadastroVaga.setModel(modeloVaga);        
    
     }
    
